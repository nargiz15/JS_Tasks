
// 1.  prompt vasitesile direction (istiqamet) alin. eger up-dirsa, console-a yuxari get, right -> saga get, 
// down -> asagi get, left -> sola get sozlerini cixarin. 



let direction =prompt("istiqamet sec")
switch (direction){
    case ("up"):
        console.log("yuxari get");
        break

    case ("right"):
        console.log("saga get");
        break

    case ("down"):
        console.log("asagi get");
        break

    case ("left"):
        console.log("sola get");
        break
}




